    private void btnCadastrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCadastrarMouseClicked
        MArmazenamento arm=new MArmazenamento();
        DAOArmazenamento daoarm=new DAOArmazenamento();       
        boolean check=true;
        
        
            if(txt_modelo.getText().isEmpty()    || txt_Fab.getText().isEmpty() 
            || txt_slot.getText().isEmpty() || txt_consumo.getText().isEmpty()
            || txt_preco.getText().isEmpty()
            ){
                JOptionPane.showMessageDialog(null,"Por favor, preencha todos os campos corretamente.");
                check=false;
            }


            if(check=true){
                arm.setMODELO(txt_modelo.getText());
                arm.setFABRICANTE(txt_Fab.getText());
                arm.setSLOT(txt_slot.getText());
                arm.setCONSUMO(Integer.parseInt(txt_consumo.getText()));
                arm.setPRECO(Float.parseFloat(txt_preco.getText()));
                
                if(btnCadastrar.getText().equals("Cadastrar")){ 
                    try {
                        daoarm.inserir(arm,"");
                        JOptionPane.showMessageDialog(null,"Armazenamento cadastrado com sucesso!");
                    } catch (SQLException ex) {
                        Logger.getLogger(PlacaMae.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                   esvaziarCampos();
            }
        
    }        
    }                                 
    private void esvaziarCampos(){
        txt_modelo.setText("");
        txt_preco.setText("");
        txt_consumo.setText("");
        txt_slot.setText("");
        txt_Fab.setText("");
    }//GEN-LAST:event_btnCadastrarMouseClicked

