    private void btnCadastrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCadastrarMouseClicked
        MPlacaMae placa=new MPlacaMae();
        DAOPlaca daoplaca=new DAOPlaca();       

        
        boolean check=true;
        
        
            if(txt_nome.getText().isEmpty()    || txt_Fab.getText().isEmpty() 
            || txt_socket.getText().isEmpty() || txt_tiposata.getText().isEmpty()
            || txt_qtdsata.getText().isEmpty() || txt_vermem.getText().isEmpty() 
            || txt_fmem.getText().isEmpty()    || txt_memax.getText().isEmpty()
            || txt_qtdpci.getText().isEmpty()  || txt_m2.getText().isEmpty()
            || txt_ide.getText().isEmpty()     || txt_preco.getText().isEmpty()
            ){
                JOptionPane.showMessageDialog(null,"Por favor, preencha todos os campos corretamente");
                check=false;
            }


            if(check=true){
                placa.setNOME(txt_nome.getText());
                placa.setFABRICANTE(txt_Fab.getText());
                placa.setSOCKET(txt_socket.getText());
                placa.setTIPOSATA(txt_tiposata.getText());
                placa.setQTDSATA(Integer.parseInt(txt_qtdsata.getText()));
                placa.setVERMEM(txt_vermem.getText());
                placa.setFMEM(Integer.parseInt(txt_fmem.getText()));
                placa.setMEMAX(Integer.parseInt(txt_memax.getText()));
                placa.setQTDPCI(Integer.parseInt(txt_qtdpci.getText()));
                placa.setM2(Boolean.parseBoolean(txt_m2.getText()));
                placa.setIDE(Boolean.parseBoolean(txt_ide.getText()));
                placa.setPRECO(Float.parseFloat(txt_preco.getText()));

                if(btnCadastrar.getText().equals("Cadastrar")){ 

                    try {
                        daoplaca.inserir(placa,"");
                        JOptionPane.showMessageDialog(null,"Placa M√£e cadastrada com sucesso!");
                    } catch (SQLException ex) {
                        Logger.getLogger(PlacaMae.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                   esvaziarCampos();
            }
    }//GEN-LAST:event_btnCadastrarMouseClicked
    }
